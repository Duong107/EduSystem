    private void date() {
        Calendar calendar = new GregorianCalendar();
        int datemonth = calendar.get(Calendar.DATE);
        int day = calendar.get(Calendar.DAY_OF_WEEK);
        int month = calendar.get(Calendar.MONTH);
        int year = calendar.get(Calendar.YEAR);
        if (day == 1) {
            lbshowdate.setText("Chủ Nhật,Ngày " + String.valueOf(datemonth) + " Tháng " + String.valueOf(month) + " Năm " + String.valueOf(year));
        } else {
            lbshowdate.setText("Thứ " + String.valueOf(day) + " Ngày " + String.valueOf(datemonth) + " Tháng " + String.valueOf(month) + " Năm " + String.valueOf(year));
        }
    }

    private void timenow() {
        Thread clock = new Thread() {
            public void run() {
                try {
                    while (true) {
                        Calendar cal = new GregorianCalendar();
                        int h = cal.get(Calendar.HOUR_OF_DAY);
                        int m = cal.get(Calendar.MINUTE);
                        int s = cal.get(Calendar.SECOND);
                        String time = h + " : " + m + " : " + s;
                        lbshowtime.setText(time);
                        sleep(1000);
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        };
        clock.start();
    }

