public void exportdoc(JTable table) {
        JFileChooser chooser = new JFileChooser();
        int i = chooser.showSaveDialog(chooser);
        if (i == JFileChooser.APPROVE_OPTION) {
            File file = chooser.getSelectedFile();
            try {
                OutputStream outputStream = new FileOutputStream(file + ".doc");
                OutputStreamWriter outputStreamWriter = new OutputStreamWriter(outputStream, "UTF-8");// json is your data 
                DefaultTableModel model = (DefaultTableModel) table.getModel();
                for (int j = 0; j < table.getColumnCount(); j++) {
                    outputStreamWriter.write(model.getColumnName(j) + "\t");
                }
                outputStreamWriter.write("\n");
                for (int j = 0; j < table.getRowCount(); j++) {
                    for (int k = 0; k < table.getColumnCount(); k++) {
                        outputStreamWriter.write(model.getValueAt(j, k) + "\t");
                    }
                    outputStreamWriter.write("\n");
                }
                outputStreamWriter.flush();
                outputStreamWriter.close();
                JOptionPane.showMessageDialog(null, "Lưu file thành công!");
            } catch (Exception e2) {
                JOptionPane.showMessageDialog(null, "Lỗi khi lưu file!");
            }
        }
    }
