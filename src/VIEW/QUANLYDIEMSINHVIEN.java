    private void filltable(String lop) {
        model = (DefaultTableModel) tbdiem.getModel();
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
        try {
            String commandquery = "select * from sinhvien FULL OUTER JOIN diem on sinhvien.masv = diem.masv where sinhvien.malop ='" + lop + "'";
            Connection con = DriverManager.getConnection(url, user, pass);
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(commandquery);
            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("masv"), rs.getString("hotensv"), rs.getString("tienganh"), rs.getString("java3"), rs.getString("agile"), rs.getString("python"), rs.getString("vothuat"), rs.getString("diemtb")});
            }
            for (int i = 0; i < model.getRowCount(); i++) {
                for (int j = 0; j < model.getColumnCount(); j++) {
                    if (model.getValueAt(i, j) == null) {
                        model.setValueAt("0", i, j);
                    }
                }
            }
            model.fireTableDataChanged();
            rs.close();
            con.close();
            System.out.println("Load Table!");
        } catch (Exception e) {
            ImageIcon img = new ImageIcon("src/DAO/ICON/ERROR.png");
            JOptionPane.showMessageDialog(this, "liên kết bị lỗi\n" + e, "ERROR", 0, img);
        }
    }

